package praticasDia1;

public class CaracteresEspeciais {

	public static void main(String[] args) {
		System.out.println("Testando caracteres especiais:\n");
		
		System.out.println("Nova linha\n");
		System.out.println("Usando o tab\t");
		System.out.println("Usando backspace\b");
		System.out.println("Retorno de carro\r");
		System.out.println("Avanca na impressora\f");
		System.out.println("Barra invertida\\");
		System.out.println("Apostrofe\'");
		System.out.println("As aspas\"");
		
		
	}
	
	/*
	 * sequencia de escape
	 * 
	 * \n - nova linha
	 * \t - tab
	 * \b - backspace
	 * \r - retorno de carro
	 * \f - avanca força na impressora
	 * \\ - barra invertida
	 * \' - apostrofe
	 * \" - aspas	 * 
	 */

}
